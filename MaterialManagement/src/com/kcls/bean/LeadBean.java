package com.kcls.bean;

public class LeadBean {

	private String lead_id;
	private String lead_name;
	private Integer lead_level;
	private Double approval_money;
	private String lead_remark;
	public String getLead_id() {
		return lead_id;
	}
	public void setLead_id(String lead_id) {
		this.lead_id = lead_id;
	}
	public String getLead_name() {
		return lead_name;
	}
	public void setLead_name(String lead_name) {
		this.lead_name = lead_name;
	}
	public Integer getLead_level() {
		return lead_level;
	}
	public void setLead_level(Integer lead_level) {
		this.lead_level = lead_level;
	}
	public Double getApproval_money() {
		return approval_money;
	}
	public void setApproval_money(Double approval_money) {
		this.approval_money = approval_money;
	}
	public String getLead_remark() {
		return lead_remark;
	}
	public void setLead_remark(String lead_remark) {
		this.lead_remark = lead_remark;
	}
	
}
