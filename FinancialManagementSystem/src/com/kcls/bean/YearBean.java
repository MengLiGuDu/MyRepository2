package com.kcls.bean;

public class YearBean {

	private String year_id;
	private String year_beginDate;
	private String year_finishDate;
	private String year_name;
	private String create_name;
	private String create_date;
	
	public String getYear_id() {
		return year_id;
	}
	public void setYear_id(String year_id) {
		this.year_id = year_id;
	}
	public String getYear_beginDate() {
		return year_beginDate;
	}
	public void setYear_beginDate(String year_beginDate) {
		this.year_beginDate = year_beginDate;
	}
	public String getYear_finishDate() {
		return year_finishDate;
	}
	public void setYear_finishDate(String year_finishDate) {
		this.year_finishDate = year_finishDate;
	}
	public String getYear_name() {
		return year_name;
	}
	public void setYear_name(String year_name) {
		this.year_name = year_name;
	}
	public String getCreate_name() {
		return create_name;
	}
	public void setCreate_name(String create_name) {
		this.create_name = create_name;
	}
	public String getCreate_date() {
		return create_date;
	}
	public void setCreate_date(String create_date) {
		this.create_date = create_date;
	}

	
}
